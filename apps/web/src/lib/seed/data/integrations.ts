const REASONS: Record<string, string[]> = {
  "INT-shopee": ["AUTH_TOKEN_EXPIRED", "AUTH_REFRESH_FAILED"],
  "INT-tiktok": ["RATE_LIMITED", "PARTIAL_PAGE_TIMEOUT"],
  "INT-rudderstack": ["SCHEMA_DRIFT", "WAREHOUSE_JOB_TIMEOUT"],
  "INT-ninja-van": ["WEBHOOK_GAP_DETECTED"],
  "INT-stripe": ["TRANSIENT_5XX"],
};

const rng = mulberry32(0xef01);

export const SYNC_RUNS: SyncRun[] = INTEGRATIONS.flatMap((integration) => {
  const runs: SyncRun[] = [];
  const cadenceHours = Math.max(integration.freshnessSlaMinutes / 60, 0.5);
  for (let i = 0; i < 8; i++) {
    const started = hoursAgo(0.3 + i * cadenceHours);
    const isStaleSource = integration.status === "stale";
    const failing = isStaleSource && i < 6;
    const partial = !failing && integration.status === "degraded" && i % 3 === 1;
    const status = failing ? "failed" : partial ? "partial" : "success";
    const reasons = REASONS[integration.id];
    runs.push({
      id: `SYN-${integration.id.slice(4)}-${i}`,
      integrationId: integration.id,
      startedAt: started,
      finishedAt: started,
      status,
      recordsRead: status === "failed" ? 0 : intBetween(rng, 40, 900),
      recordsWritten: status === "failed" ? 0 : intBetween(rng, 40, 900),
      errorCount: status === "failed" ? intBetween(rng, 1, 4) : status === "partial" ? 1 : 0,
      reasonCode: status === "success" ? null : (reasons ? reasons[i % reasons.length]! : "UNKNOWN_ERROR"),
      message:
        status === "failed"
          ? "Run aborted — see reason code. Checkpoint not advanced."
          : status === "partial"
            ? "Completed with skipped pages; retry scheduled."
            : "Completed. Checkpoint advanced.",
    });
  }
  return runs;
});
